    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String Code = usercode.getText();
        String Name = name.getText();
        Double Salary = Double.parseDouble(salary.getText());
        String Address = address.getText();

        //Setting Values into Model Object
        User u = new User();
        u.setUserCode(Code);
        u.setName(Name);
        u.setSalary(Salary);
        u.setAddress(Address);

        //Setting Value into Service
        EmployeeService employeeservice = new EmployeeService();
        employeeservice.addEmployee(u);

        ResetInput();

    }//GEN-LAST:event_saveActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        String Code = usercode.getText();
        String Name = name.getText();
        Double Salary = Double.parseDouble(salary.getText());
        String Address = address.getText();
        EmployeeService employeeservice = new EmployeeService();
//        User u = employeeservice.updateDao(empId);
        User u = new User();
        u.setUserCode(Code);
        u.setName(Name);
        u.setSalary(Salary);
        u.setAddress(Address);
        u.setId(empId);
        int status = employeeservice.updateDao(u);
        if (status == 1) {
            JOptionPane.showMessageDialog(rootPane, "Updated successfully!");
        }
        ResetInput();
    }//GEN-LAST:event_updateActionPerformed

