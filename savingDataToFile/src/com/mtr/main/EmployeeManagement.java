    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Employee employee = new Employee();
        employee.setCode(Integer.parseInt(txtCode.getText()));
        employee.setName(txtName.getText());
        employee.setAddress(txtAddress.getText());
        employee.setSalary(Double.parseDouble(txtSalary.getText()));
        
        new DataServiceImpl().saveEmployee(employee);
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed

        Employee emp = new DataServiceImpl().getEmployee(Integer.parseInt(txtEdit.getText()));
        if(emp.getCode() != 0){
           txtCode.setText(String.valueOf(emp.getCode()));
            txtName.setText(emp.getName());
            txtAddress.setText(emp.getAddress());
            txtSalary.setText(String.valueOf(emp.getSalary()));
            id = emp.getId(); 
        }else{
            JOptionPane.showMessageDialog(null, "data not found");
        }
        

    }//GEN-LAST:event_btnEditActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        Employee employee = new Employee();
        employee.setId(id);
        employee.setCode(Integer.parseInt(txtCode.getText()));
        employee.setName(txtName.getText());
        employee.setAddress(txtAddress.getText());
        employee.setSalary(Double.parseDouble(txtSalary.getText()));

        new DataServiceImpl().updateEmployee(employee);
        id = 0;
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnGetAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGetAllActionPerformed
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        try {
            List<Employee> e = new DataServiceImpl().getAll();
            
            for (Employee emp : e) {
                int code = emp.getCode();
                String name = emp.getName();
                String address = emp.getAddress();
                double salary = emp.getSalary();
                
                model.addRow(new Object[]{code, name, address, salary});
            }
        } catch (Exception ex) {
        }
    }//GEN-LAST:event_btnGetAllActionPerformed

