    public Employee_Control() {
        try {
            initComponents();
            String selectQ = "select * from role";
            con = DbConnection.getConnection();
            ps = con.prepareStatement(selectQ);
            rs = ps.executeQuery();
            while (rs.next()) {
                cmbRole.addItem(rs.getString("role_name"));
            }
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(Employee_Control.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

