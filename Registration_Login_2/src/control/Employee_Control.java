    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        getAll();
        Employee e = new Employee();
        e.setEmp_name(name);
        e.setEmp_email(email);
        e.setEmp_userName(user_name);
        e.setEmp_password(password);
        e.setRole_name(role);
        int status = new EmployeeSevice().save(e);
        if (status > 0) {
            JOptionPane.showMessageDialog(rootPane, "Employee inserted !");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Employee couldn't inserted !");

        }
    }//GEN-LAST:event_btnAddActionPerformed

