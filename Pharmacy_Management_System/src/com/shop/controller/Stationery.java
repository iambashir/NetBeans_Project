    private void searchByCodeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchByCodeActionPerformed
         try {
            con = DBConnection.getConnection();
            prst = con.createStatement();
            String sql = "select * from product_entry where barcode = ?";

            PreparedStatement prst = con.prepareStatement(sql);
            prst.setString(1, searchByProductField1.getText());

            ResultSet rs = prst.executeQuery();
            if (rs.next()) {

                barcode.setText(rs.getString("barcode"));
                productName.setText(rs.getString("product_name"));
                branch.setText(rs.getString("branch"));
                qt.setText(String.valueOf(rs.getInt("quantity")));
                unitPrice.setText(String.valueOf(rs.getDouble("unit_price")));
                disscount.setText(String.valueOf(rs.getDouble("discount")));
                categories.setText(rs.getString("catagories"));
                inputerName.setText(rs.getString("inputer_name"));
                phone.setText(rs.getString("phone"));
                date.setText(rs.getString("date")); 
                
            } else {
                JOptionPane.showMessageDialog(null, "Record Not found");
            }
            con.close(); //after completing task close the popup window
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_searchByCodeActionPerformed

