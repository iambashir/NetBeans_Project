    private void updateStockTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateStockTableActionPerformed
        try {
            con = DBConnection.getConnection();
            prst = con.createStatement();
            String sql = "update product_entry set product_name = ?, branch = ?, quantity = ?, unit_price = ?, discount = ?, catagories = ?, inputer_name = ?, phone = ?, date = ? where barcode = ?";

            PreparedStatement prst = con.prepareStatement(sql);
            
            prst.setString(1, productName.getText());
           // prst.setInt(2, Integer.parseInt(branch.getText()));
            prst.setString(2, branch.getText());
            prst.setInt(3, Integer.parseInt(qt.getText()));
            prst.setDouble(4, Double.parseDouble(unitPrice.getText()));
            prst.setDouble(5, Double.parseDouble(disscount.getText()));
            prst.setString(6, categories.getText());
            prst.setString(7, inputerName.getText());
            prst.setString(8, phone.getText());
            prst.setString(9, date.getText());

            prst.setString(10, barcode.getText());
            
            

            prst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Record Updated successfully");

          
            table_update();//Refresh Table

            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_updateStockTableActionPerformed

