        private void table_update() {
        int c;
        try {
            con = DBConnection.getConnection();
             prst = con.createStatement();
            String sql = "select * from product_entry where catagories = 'Grocery' "; // 
            
            PreparedStatement prst = con.prepareStatement(sql);
            ResultSet rs = prst.executeQuery();
            ResultSetMetaData Rss = rs.getMetaData();
            c = Rss.getColumnCount();
            DefaultTableModel dr = (DefaultTableModel) stocksTable1.getModel();
            dr.setRowCount(0);
            while (rs.next()) {
                Vector v2 = new Vector();
                for (int a = 1; a <= c; a++) {
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("barcode"));
                    v2.add(rs.getString("product_name"));
                    v2.add(rs.getString("branch"));
                    v2.add(rs.getString("quantity"));
                    v2.add(rs.getString("unit_price"));
                    v2.add(rs.getString("discount"));
                    v2.add(rs.getString("catagories"));
                    v2.add(rs.getString("inputer_name"));
                    v2.add(rs.getString("phone"));
                    v2.add(rs.getString("date"));
                    
                }
                dr.addRow(v2);
            }

        } catch (SQLException ex) {
           // Logger.getLogger(StudentRegistrationForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
 
        
