    private void btnvalidateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnvalidateActionPerformed
        String email = txtemail.getText();
        if(email.length() < 8 || email.length() > 8){
            JOptionPane.showMessageDialog(this, "email address should be 8 characters");
        }else{
            if(email.contains("@") && email.contains(".")){
                JOptionPane.showConfirmDialog(this, "email is validated");
            }else{
                JOptionPane.showMessageDialog(this, "email must contain @ and . symbols");
            }
        }
    }//GEN-LAST:event_btnvalidateActionPerformed

