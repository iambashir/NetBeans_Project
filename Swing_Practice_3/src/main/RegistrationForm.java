          try{
    //======================Same code for every connection==============================//
    Class.forName("com.mysql.jdbc.Driver");  
    Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/bashir_swing_practice", "root", "1234");
    String sql = "insert into home_login values (?, ?, ?, ?)"; // "?" is for table row number and home_login is database table name
    PreparedStatement prst = con.prepareStatement(sql);

    //=====================Add/save/insert your data=====================================//
    prst.setString(3, txtName.getText());
    prst.setString(4, txtAge.getText()); 
    prst.setString(1, txtUserName.getText()); 
    prst.setString(2, txtPassword.getText()); 

    prst.executeUpdate();
    JOptionPane.showMessageDialog(null, "Registerd successfully"); //after submission popup massage window
    con.close(); //after completing task close the popup window
    } 
    catch(Exception e)
    {
    JOptionPane.showMessageDialog(null, e);
    } 

        
