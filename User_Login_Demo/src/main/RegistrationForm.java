    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        getValues();
        if (name.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Please enter your name");
            txtName.requestFocus();
        } else if (name.length() < 4) {
            JOptionPane.showMessageDialog(rootPane, "Name should be 4 character long");
            txtName.requestFocus();
        } else if (usrName.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Please enter your user name");
            txtUserName.requestFocus();
        } else if (usrPaasword.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Please enter your Password");
            txtPassword.requestFocus();
        } else if (usrPaasword.length() < 4) {
            JOptionPane.showMessageDialog(rootPane, "Password shoud be 4 character long");
            txtPassword.requestFocus();
        } else {
            User u = new User();
            u.setName(name);
            u.setAge(age);
            u.setUsername(usrName);
            u.setPassword(usrPaasword);
            int status = new User_Service().addUser(u);
            if (status > 0) {
                JOptionPane.showMessageDialog(rootPane, "User saved!");
                txtName.setText("");
                txtAge.setText("");
                txtUserName.setText("");
                txtPassword.setText("");
            } else {
                JOptionPane.showMessageDialog(rootPane, "User couldn't saved!");

            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

