    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
       String code ,model, processor;
       int ram;
       code = txtcode.getText();
       model = txtmodel.getText();
       processor = txtprocessor.getText();
       ram = Integer.parseInt(txtram.getText());
       Regmobile rm = new Regmobile();
       rm.setCode(code);
       rm.setModel(model);
       rm.setProcessor(processor);
       rm.setRam(ram);
       int status = mobileservice.save(rm);
        if (status == 1) {
            JOptionPane.showMessageDialog(rootPane, " Saved Successfully !");
            
        } else {
            JOptionPane.showMessageDialog(rootPane, "Already exists !");
        }
    }//GEN-LAST:event_btnsaveActionPerformed

