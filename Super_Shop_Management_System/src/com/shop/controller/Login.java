private void initComboBox() {
     try{
    con = DBConnection.getConnection();
    prst = con.createStatement();
    String sql = "select distinct user_type from user"; // if want unique distinct value
    //"select * from user"; //if I want All value
    
    PreparedStatement prst = con.prepareStatement(sql);
   // prst.setInt(1, Integer.parseInt(rollField.getText()));
   
    ResultSet rs = prst.executeQuery();
    while(rs.next()){
        userComboBox.addItem(rs.getString("user_type"));
    }
    con.close();
    } 
    catch(Exception e)
    {
    JOptionPane.showMessageDialog(null, "Record Not found");
    }
}
   
