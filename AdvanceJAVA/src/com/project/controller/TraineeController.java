    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String code = traineeCode.getText();
        String name = traineeName.getText();
        String batch = traineeBatch.getText();
        int round = Integer.parseInt(traineeRound.getText());
        Trainee t = new Trainee();
        t.setCode(code);
        t.setName(name);
        t.setBatch(batch);
        t.setRound(round);
        int status = traineeService.save(t);
         if (status == 1) {
            JOptionPane.showMessageDialog(rootPane, "Trainee Saved Successfully !");
            
        } else {
            JOptionPane.showMessageDialog(rootPane, "Trainee Code already exists !");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

