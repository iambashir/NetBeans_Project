    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String N;
        char[] P;
        N = tuname.getText();
        P = tpass.getPassword();//return char array
        String pass = String.valueOf(P);//convert char array to string
        if (!N.isEmpty()) {
            if (!pass.isEmpty()) {
                Connection con = null;
                ResultSet rs = null;
                try {
                    con = DatabaseConnector.getConnection();
                    Statement stmt;
                    stmt = con.createStatement();
                    int count = 0;
                    rs = stmt.executeQuery("select * from admin_login where ausername='"+N+"' and apassword='" + pass + "' ");

                    while (rs.next()) {
                        count = rs.getRow();
                    }
                    if (count > 0) {
//                        JOptionPane.showMessageDialog(null, "Login Successful!");
                            lblshowlogs.setText("Login Successful!");
                        CManagement cm= new CManagement();
                        cm.setVisible(true);
                        this.dispose();

                    } else {
//                        JOptionPane.showMessageDialog(null, "Login Fail!");
                            lblshowlogs.setText("Login Fail!");
                    }

                    rs.close();
                    stmt.close();
                    con.close();
                } catch (Exception e) {
                    e.printStackTrace();
                }


            } else {
                JOptionPane.showMessageDialog(null, "Please Enter Password!");
                tpass.requestFocus();
            }



        } else {
            JOptionPane.showMessageDialog(null, "Please Enter User Name!");
            tuname.requestFocus();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

